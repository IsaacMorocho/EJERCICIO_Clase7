public class HAMSTER {
    double tamaño;
    int edad;

    //Constructor vacio
    public HAMSTER(){
    }
    //SETTERS
    public void setTamaño(){
        this.tamaño=tamaño;
    }
    public void setEdad(){
        this.edad=edad;
    }
    //Metodos personalizados
    public void mostrarTamañoH(){
        System.out.println("El tamaño del hamster es: "+tamaño);
    }
    public void mostrarEdadH(){
        System.out.println("La edad del hamster es: "+edad);
    }
}

